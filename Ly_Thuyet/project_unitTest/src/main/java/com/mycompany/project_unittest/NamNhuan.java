public class NamNhuan {

    public boolean IsLeapYear(int year) {
        if (year == 0) {
            return false;
        }
        if ((year % 4 == 0 && year % 100 != 0) || (year % 400 == 0)) {
            return true;
        }
        return false;
    }
}
